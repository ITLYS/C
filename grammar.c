#include <stdio.h>
/*C语法：左位移 和 右位移*/
int main()
{
   int i=1;

   i=i<<2;
   //      i 转化二进制 00000001  => 1
   //i<<4  i 转化二进制 00000100  => 4
   printf("i=%d\n",i);

   i=i<<4;
   //      i 转化二进制 00000100  => 4
   //i<<4  i 转化二进制 01000000  => 64
   printf("i=%d\n",i);

   i=i>>1;
   //      i 转化二进制 01000000  => 64
   //i>>4  i 转化二进制 00100000  => 32
   printf("i=%d\n",i);

   i=i>>5;
   //      i 转化二进制 00100000  => 32
   //i>>5  i 转化二进制 00000001  => 1
   printf("i=%d",i);
}
